export interface SkillEffect {
  type:
    | 'burn'
    | 'freeze'
    | 'shock'
    | 'bleed'
    | 'entangle'
    | 'shield'
    | 'atkUp'
    | 'defUp'
    | 'spdUp'
    | 'critUp'
    | 'atkDown'
    | 'defDown'
    | 'spdDown'
    | 'healOverTime'
  value: number // percentage (e.g. 30 = 30%)
  duration: number // turns
  chance: number // 0-100
}

export type SkillTarget =
  | 'single'
  | 'allEnemies'
  | 'self'
  | 'singleAlly'
  | 'allAllies'

export interface CharacterSkill {
  name: string
  description: string
  multiplier: number // % of ATK as damage, or % of maxHP as heal
  target: SkillTarget
  spChange: number // +N = generate SP, -N = consume SP
  energyGen: number
  effects: SkillEffect[]
}

export interface UltimateSkill {
  name: string
  description: string
  multiplier: number
  target: SkillTarget
  energyCost: number
  effects: SkillEffect[]
}

export interface TalentPassive {
  name: string
  description: string
}

export interface CharacterTemplate {
  id: string
  name: string
  rarity: 4 | 5
  element: Element
  path: Path
  emoji: string
  description: string
  baseHP: number
  baseATK: number
  baseDEF: number
  baseSPD: number
  hpGrowth: number
  atkGrowth: number
  defGrowth: number
  basic: CharacterSkill
  skill: CharacterSkill
  ultimate: UltimateSkill
  talent: TalentPassive
}

// Helper to compute stats at a given level
export function getCharacterStats(char: CharacterTemplate, level: number) {
  return {
    hp: char.baseHP + char.hpGrowth * (level - 1),
    atk: char.baseATK + char.atkGrowth * (level - 1),
    def: char.baseDEF + char.defGrowth * (level - 1),
    spd: char.baseSPD,
  }
}

